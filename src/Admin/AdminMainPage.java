    private void houseDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_houseDetailsActionPerformed
        // TODO add your handling code here:
        
        String houseType = houseDetails.getSelectedItem().toString();
        if("     Mess Details".equals(houseType))
        {
            MessDetails md = new MessDetails(aemail, aname);
            md.setVisible(true);
            this.setVisible(false);
        }
        else if("     Flat Details".equals(houseType))
        {
            FlatDetails fd = new FlatDetails(aemail, aname);
            fd.setVisible(true);
            this.setVisible(false);
        }
        if("     Sub-Let Details".equals(houseType))
        {
            SubLetDetails sd = new SubLetDetails(aemail, aname);
            sd.setVisible(true);
            this.setVisible(false);
        }
        
    }//GEN-LAST:event_houseDetailsActionPerformed

